    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        // TODO add your handling code here:
                                                  
    String name = fieldName.getText();
    String username = fieldUsername.getText();
    String password = fieldPassword.getText();

    if (name.trim().isEmpty() || username.trim().isEmpty() || password.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields are required!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    PersonDirectory persondirectory = business.getPersonDirectory();
    Person newperson = persondirectory.newPerson(name);

    StudentDirectory studentdirectory = business.getStudentDirectory();
    StudentProfile studentprofile = studentdirectory.newStudentProfile(newperson);

    UserAccountDirectory uadirectory = business.getUserAccountDirectory();
    UserAccount useraccount = uadirectory.newUserAccount(studentprofile, username, password);

    JOptionPane.showMessageDialog(this, "Sign up successful! You can now login.", "Success", JOptionPane.INFORMATION_MESSAGE);

    CardSequencePanel.remove(this);
    ((java.awt.CardLayout) CardSequencePanel.getLayout()).next(CardSequencePanel);

    }//GEN-LAST:event_btnSignUpActionPerformed

